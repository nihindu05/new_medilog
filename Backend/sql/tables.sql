    CONSTRAINT fk_lab_request_laboratory
        FOREIGN KEY (laboratory_id) REFERENCES laboratory(laboratory_id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_lab_request_requested_by
        FOREIGN KEY (requested_by_doctor_id) REFERENCES doctor(doctor_id)
        ON UPDATE CASCADE ON DELETE SET NULL
