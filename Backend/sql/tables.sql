CREATE TABLE examination (
    examination_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    case_id varchar(30) NOT NULL,
    exam_type varchar(100) NOT NULL,
    exam_datetime timestamptz NOT NULL,
    exam_place varchar(300),
    examination_status varchar(100) NOT NULL DEFAULT 'Scheduled',
    notes text,
    created_by_user_id integer NOT NULL,
    CONSTRAINT fk_examination_case
        FOREIGN KEY (case_id) REFERENCES forensic_case(case_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_examination_created_by
        FOREIGN KEY (created_by_user_id) REFERENCES user_account(user_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE examination_doctor (
    examination_doctor_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    examination_id integer NOT NULL,
    doctor_id integer NOT NULL,
    role_in_examination varchar(100) NOT NULL,
    CONSTRAINT uq_examination_doctor_role
        UNIQUE (examination_id, doctor_id, role_in_examination),
    CONSTRAINT fk_examination_doctor_examination
        FOREIGN KEY (examination_id) REFERENCES examination(examination_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_examination_doctor_doctor
        FOREIGN KEY (doctor_id) REFERENCES doctor(doctor_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE injury (
    injury_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    examination_id integer NOT NULL,
    injury_type varchar(100) NOT NULL,
    body_location varchar(200) NOT NULL,
    side varchar(30),
    length numeric(8,2),
    width numeric(8,2),
    depth numeric(8,2),
    description text,
    injury_age varchar(100),
    severity varchar(100),
    weapon_opinion varchar(300),
    is_fatal boolean NOT NULL DEFAULT false,
    CONSTRAINT chk_injury_dimensions
        CHECK (
            (length IS NULL OR length >= 0)
            AND (width IS NULL OR width >= 0)
            AND (depth IS NULL OR depth >= 0)
        ),
    CONSTRAINT fk_injury_examination
        FOREIGN KEY (examination_id) REFERENCES examination(examination_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE internal_finding (
    internal_finding_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    examination_id integer NOT NULL,
    organ_name varchar(150) NOT NULL,
    finding_description text NOT NULL,
    pathological_condition varchar(500),
    CONSTRAINT fk_internal_finding_examination
        FOREIGN KEY (examination_id) REFERENCES examination(examination_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE body_diagram_marking (
    marking_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    examination_id integer NOT NULL,
    injury_id integer,
    body_region varchar(200) NOT NULL,
    x_coordinate numeric(9,3),
    y_coordinate numeric(9,3),
    marking_description text,
    CONSTRAINT fk_body_diagram_marking_examination
        FOREIGN KEY (examination_id) REFERENCES examination(examination_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_body_diagram_marking_injury
        FOREIGN KEY (injury_id) REFERENCES injury(injury_id)
        ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE sample_evidence (
    sample_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sample_reference varchar(50) UNIQUE,
    case_id varchar(30) NOT NULL,
    examination_id integer,
    sample_type varchar(150) NOT NULL,
    collected_datetime timestamptz NOT NULL,
    collected_by_doctor_id integer,
    seal_no varchar(100),
