CREATE TABLE laboratory (
    laboratory_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    laboratory_name varchar(200) NOT NULL,
    location varchar(300),
    contact_no varchar(30)
);

CREATE TABLE lab_request (
    lab_request_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_reference varchar(50) UNIQUE,
    sample_id integer NOT NULL,
    laboratory_id integer NOT NULL,
    requested_test_type varchar(150) NOT NULL,
