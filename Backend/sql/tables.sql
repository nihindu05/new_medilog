-- ---------------------------------------------------------------------------
-- 3. PATIENT / VICTIM / DECEASED PERSON TABLES
-- ---------------------------------------------------------------------------

CREATE TABLE patient_victim (
    patient_id varchar(30) PRIMARY KEY,
    full_name varchar(200) NOT NULL,
    nic_passport_no varchar(80),
    date_of_birth date,
    age smallint,
    gender varchar(30),
    permanent_address varchar(500),
    contact_no varchar(30),
    hospital_no varchar(100),
    bht_no varchar(100),
    nationality varchar(100),
    ethnicity varchar(100),
    person_status varchar(50) NOT NULL,
    is_minor boolean NOT NULL DEFAULT false,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_patient_victim_nic_passport_no UNIQUE (nic_passport_no),
    CONSTRAINT chk_patient_age CHECK (age IS NULL OR age BETWEEN 0 AND 150),
    CONSTRAINT chk_patient_person_status
        CHECK (person_status IN ('Living', 'Deceased', 'Unknown'))
);

CREATE TRIGGER trg_patient_victim_set_updated_at
BEFORE UPDATE ON patient_victim
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

CREATE TABLE living_patient_details (
    patient_id varchar(30) PRIMARY KEY,
    current_condition varchar(255),
    consent_status varchar(100),
    guardian_name varchar(200),
    guardian_contact varchar(30),
    CONSTRAINT fk_living_patient_details_patient
        FOREIGN KEY (patient_id) REFERENCES patient_victim(patient_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE deceased_person_details (
    patient_id varchar(30) PRIMARY KEY,
    date_of_death date,
    time_of_death time,
    place_of_death varchar(500),
    occupation varchar(150),
    identification_status varchar(100),
    body_received_datetime timestamptz,
    CONSTRAINT fk_deceased_person_details_patient
        FOREIGN KEY (patient_id) REFERENCES patient_victim(patient_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE next_of_kin (
    next_of_kin_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id varchar(30) NOT NULL,
    full_name varchar(200) NOT NULL,
    relationship varchar(100) NOT NULL,
    contact_no varchar(30),
    address varchar(500),
    CONSTRAINT fk_next_of_kin_patient
        FOREIGN KEY (patient_id) REFERENCES patient_victim(patient_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

-- ---------------------------------------------------------------------------
-- 4. CASE REGISTRATION AND CASE SPECIALISATION TABLES
-- ---------------------------------------------------------------------------

CREATE TABLE forensic_case (
    case_id varchar(30) PRIMARY KEY,
    case_type varchar(50) NOT NULL,
    case_category varchar(100),
    case_status varchar(100) NOT NULL DEFAULT 'Registered',
    registered_datetime timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    patient_id varchar(30) NOT NULL,
    registered_by_user_id integer NOT NULL,
    jmo_office_id integer NOT NULL,
    confidentiality_level varchar(50) NOT NULL DEFAULT 'Standard',
    is_sexual_assault_case boolean NOT NULL DEFAULT false,
    CONSTRAINT chk_forensic_case_type
        CHECK (case_type IN ('Clinical', 'Autopsy')),
    CONSTRAINT fk_forensic_case_patient
        FOREIGN KEY (patient_id) REFERENCES patient_victim(patient_id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_forensic_case_registered_by
        FOREIGN KEY (registered_by_user_id) REFERENCES user_account(user_id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_forensic_case_jmo_office
        FOREIGN KEY (jmo_office_id) REFERENCES jmo_office(jmo_office_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE clinical_case_details (
    case_id varchar(30) PRIMARY KEY,
    mlef_no varchar(100),
    mlef_issued_date date,
    reason_for_examination varchar(500),
    patient_history text,
    alcohol_consumption_status varchar(100),
    drug_consumption_status varchar(100),
    nature_of_harm varchar(300),
    nature_of_weapon varchar(300),
    hurt_category varchar(100),
    mlr_serial_no varchar(100),
    CONSTRAINT uq_clinical_case_mlef_no UNIQUE (mlef_no),
    CONSTRAINT fk_clinical_case_details_case
        FOREIGN KEY (case_id) REFERENCES forensic_case(case_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE autopsy_case_details (
    case_id varchar(30) PRIMARY KEY,
    pm_registry_no varchar(100),
    inquest_no varchar(100),
    court_order_no varchar(100),
    death_report_no varchar(100),
    date_of_inquest date,
    place_of_pm varchar(300),
    cause_of_death_summary text,
    manner_of_death varchar(100),
    CONSTRAINT uq_autopsy_case_pm_registry_no UNIQUE (pm_registry_no),
    CONSTRAINT fk_autopsy_case_details_case
        FOREIGN KEY (case_id) REFERENCES forensic_case(case_id)
        ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE hospital_admission (
    admission_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    case_id varchar(30) NOT NULL,
    hospital_id integer NOT NULL,
    ward_id integer,
    bed_no varchar(50),
    admitted_datetime timestamptz,
    discharged_datetime timestamptz,
    CONSTRAINT chk_hospital_admission_dates
        CHECK (
            discharged_datetime IS NULL
            OR admitted_datetime IS NULL
            OR discharged_datetime >= admitted_datetime
        ),
    CONSTRAINT fk_hospital_admission_case
        FOREIGN KEY (case_id) REFERENCES forensic_case(case_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_hospital_admission_hospital
        FOREIGN KEY (hospital_id) REFERENCES hospital(hospital_id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_hospital_admission_ward
        FOREIGN KEY (ward_id) REFERENCES ward(ward_id)
        ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE case_doctor (
    case_doctor_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    case_id varchar(30) NOT NULL,
    doctor_id integer NOT NULL,
    role_in_case varchar(100) NOT NULL,
    assigned_date date NOT NULL,
    is_primary boolean NOT NULL DEFAULT false,
    status varchar(50) NOT NULL DEFAULT 'Assigned',
    CONSTRAINT uq_case_doctor_role UNIQUE (case_id, doctor_id, role_in_case),
    CONSTRAINT fk_case_doctor_case
        FOREIGN KEY (case_id) REFERENCES forensic_case(case_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_case_doctor_doctor
        FOREIGN KEY (doctor_id) REFERENCES doctor(doctor_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

