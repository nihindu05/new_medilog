-- ---------------------------------------------------------------------------
-- 0. SHARED FUNCTION FOR AUTOMATIC UPDATED_AT VALUES
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

-- ---------------------------------------------------------------------------
-- 1. ORGANISATIONAL, STAFF, USER, AND ROLE TABLES
-- ---------------------------------------------------------------------------

CREATE TABLE hospital (
    hospital_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hospital_name varchar(200) NOT NULL,
    district varchar(100) NOT NULL,
    address varchar(500)
);

CREATE TABLE jmo_office (
    jmo_office_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    office_name varchar(200) NOT NULL,
    hospital_id integer NOT NULL,
    district varchar(100) NOT NULL,
    contact_no varchar(30),
    CONSTRAINT fk_jmo_office_hospital
        FOREIGN KEY (hospital_id) REFERENCES hospital(hospital_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE ward (
    ward_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hospital_id integer NOT NULL,
    ward_name varchar(120) NOT NULL,
    ward_type varchar(100),
    CONSTRAINT uq_ward_hospital_name UNIQUE (hospital_id, ward_name),
    CONSTRAINT fk_ward_hospital
        FOREIGN KEY (hospital_id) REFERENCES hospital(hospital_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE doctor (
    doctor_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name varchar(200) NOT NULL,
    designation varchar(150) NOT NULL,
    slmc_reg_no varchar(50),
    contact_no varchar(30),
    current_office_id integer,
    employment_status varchar(50) NOT NULL DEFAULT 'Active',
    CONSTRAINT uq_doctor_slmc_reg_no UNIQUE (slmc_reg_no),
    CONSTRAINT fk_doctor_current_office
        FOREIGN KEY (current_office_id) REFERENCES jmo_office(jmo_office_id)
        ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE roles (
    role_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_name varchar(100) NOT NULL,
    description varchar(500),
    CONSTRAINT uq_role_name UNIQUE (role_name)
);

CREATE TABLE user_account (
    user_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    auth_user_id uuid,
    username varchar(100) NOT NULL,
    password_hash varchar(255),
    full_name varchar(200) NOT NULL,
    email varchar(255),
    contact_no varchar(30),
    account_status varchar(50) NOT NULL DEFAULT 'Active',
    doctor_id integer,
    jmo_office_id integer NOT NULL,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    last_login timestamptz,
    CONSTRAINT uq_user_account_username UNIQUE (username),
    CONSTRAINT uq_user_account_email UNIQUE (email),
    CONSTRAINT uq_user_account_auth_user_id UNIQUE (auth_user_id),
    CONSTRAINT fk_user_account_auth_user
        FOREIGN KEY (auth_user_id) REFERENCES auth.users(id)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_user_account_doctor
        FOREIGN KEY (doctor_id) REFERENCES doctor(doctor_id)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_user_account_jmo_office
        FOREIGN KEY (jmo_office_id) REFERENCES jmo_office(jmo_office_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE user_role (
    user_role_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id integer NOT NULL,
    role_id integer NOT NULL,
    CONSTRAINT uq_user_role UNIQUE (user_id, role_id),
    CONSTRAINT fk_user_role_user
        FOREIGN KEY (user_id) REFERENCES user_account(user_id)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_user_role_role
        FOREIGN KEY (role_id) REFERENCES roles(role_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE staff_posting (
    posting_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id integer NOT NULL,
    jmo_office_id integer NOT NULL,
    start_date date NOT NULL,
    end_date date,
    posting_status varchar(50) NOT NULL DEFAULT 'Active',
    CONSTRAINT chk_staff_posting_dates
        CHECK (end_date IS NULL OR end_date >= start_date),
    CONSTRAINT fk_staff_posting_user
        FOREIGN KEY (user_id) REFERENCES user_account(user_id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_staff_posting_jmo_office
        FOREIGN KEY (jmo_office_id) REFERENCES jmo_office(jmo_office_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

