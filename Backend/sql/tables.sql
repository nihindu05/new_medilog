    CONSTRAINT fk_sample_evidence_examination
        FOREIGN KEY (examination_id) REFERENCES examination(examination_id)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_sample_evidence_collected_by
        FOREIGN KEY (collected_by_doctor_id) REFERENCES doctor(doctor_id)
        ON UPDATE CASCADE ON DELETE SET NULL
